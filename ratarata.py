def hitung_rata_rata(no1, no2, no3):
    return (no1 + no2 + no3) / 3

no1 = float(input("masukan nomor ke-1: "))
no2 = float(input("masukan nomor ke-2: "))
no3 = float(input("masukan nomor ke-3: "))
rata_rata = hitung_rata_rata(no1, no2, no3)
print(f"rata rata dari tiga nomor tersebur=t adalah: {rata_rata}")
